// A: 0x00,0x11,0x0c
// B: 0x00,0x22,0x0c
//master: 0x00,0x00,0x0c

uint8_t receive=0,receive1=0,tmp=0;
uint8_t data1[]="\x00\x11\x0c\x31";
uint8_t data2[]="\x00\x22\x0c\x31";

UART_HandleTypeDef huart1;
UART_HandleTypeDef huart2;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART2_UART_Init(void);
static void MX_USART1_UART_Init(void);


int main(void)
{

  HAL_Init();

  SystemClock_Config();


  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_USART1_UART_Init();
	HAL_UART_Receive_IT(&huart2,&receive,1);
	HAL_UART_Receive_IT(&huart1,&receive1,1);
  while (1)
  {
		if(tmp==1)
		{
			HAL_UART_Transmit(&huart2,data2,4,200);
			HAL_Delay(5);
			HAL_UART_Transmit(&huart2,data1,4,200);
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_15|GPIO_PIN_14,GPIO_PIN_RESET);
			tmp=0;
		}
  }
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance==huart2.Instance)
	{
		HAL_UART_Receive_IT(&huart2,&receive,1);
		if(receive==0x31 )
		{
			HAL_UART_Transmit(&huart1,&receive,1,100);
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_14,GPIO_PIN_SET);
		}
		if(receive == 0x32)
		{
			HAL_UART_Transmit(&huart1,&receive,1,100);
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_15,GPIO_PIN_SET);
		}
	}
	if(huart->Instance==huart1.Instance)
	{
		HAL_UART_Receive_IT(&huart1,&receive1,1);
		if(receive1==0x31)
		{
			tmp=1;
		}
		
	}
}

